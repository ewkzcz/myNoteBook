package ewk.code01.c01prepare;

public class Main01内容辅助键与快捷键 {
    public static void main(String[] args) {
        System.out.println("Hello");
    }
    // 内容辅助键
    // Ctrl+Alt+space (代码补全)
    // sout (快速生成输出语句)

    // 快捷键
    // Ctrl+/ (单行注释)
    // Ctrl+Shift+/ (多行注释)
    // Ctrl+Alt+L (格式化)
    // Ctrl+Alt+T (选择环绕方式: try-catch块,for语句...)
    // Ctrl+Alt+V (快速生成变量名)
    // Ctrl+H(类的层次结构)
    // Ctrl+Shift+F(在项目/模块内查找)s
}